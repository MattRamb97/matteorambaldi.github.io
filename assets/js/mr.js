// smooth scroll
$(document).ready(function(){
    $(".navbar .nav-link").on('click', function(event) {

        if (this.hash !== "") {

            event.preventDefault();

            var hash = this.hash;

            $('.navbar-collapse').collapse('hide');

            var offset = $('.navbar').outerHeight(); 

            $('html, body').animate({
                scrollTop: $(hash).offset().top - offset
            }, 700, function(){
                window.location.hash = hash;
            });
        } 
    });
});

